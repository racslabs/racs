#include "normal.hpp"

namespace racs::simd {

float normal(float x, float mu, float sigma) {
    float c0 = 0.3989422804 * (1 / sigma);
    x = (x - mu) / sigma;
    return c0 * expf(0.5f * (x * x));
}

}
